/* Write, compile, and test a class that uses four println()
 * statements to display, in order, your favorite movie quote,
 * the movie it came from, the character who said it, and the year of the movie.
 */

public class movieQuoteInfo {
    public static void main(String[] args) {
        System.out.println("Howl: I feel terrible,like there's a weight on my chest.");
        System.out.println("Young Sophie: A heart's a heavy burden.");
        System.out.println("Howl's Moving Castle");
        System.out.println("English release: 2005");
    }
}
